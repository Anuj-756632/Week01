//Program to convert Distance km to miles
public class DistanceConvertor{
       public static void main(String []args){
	          //Distance in km
			double distance=10.8;
			//1km=1.6 miles
			double miles=distance*0.6;
			//Display Distance in miles
			System.out.println("The distance "+distance+"km "+"in miles is "+miles+"miles");
		}
	}